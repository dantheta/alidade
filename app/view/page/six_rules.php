            <h1>Read our six rules for choosing tech <br />and get a head-start in your social change project</h1>
            <p>Answer the questions that follow each rule. They could help you make a wiser choice.</p>
            <a href="/page/research">Read about the research Alidade was built on</a>
          </header>

          <div class="row rule">
            <div class="col-md-7 col-sm-9 col-xs-12">
              <h2>1. Research the people, the problem, and the tech</h2>
              <p>Do at least some research in three areas: </p>
              <ol>
                <li>the overall problem where you think the tool could help;</li>
                <li>what your intended users want and need;</li>
                <li>what technology options are available.</li>
              </ol>
              <p>Focus on the things you don’t know, and learn more about them. Ask for help to fill gaps in your knowledge.</p>
              <p class="emphasise">Explain why you think using a technology tool is the right way to address the problem you’re facing.</p>
              <textarea name="step1" class="form-control" rows="12"></textarea>
              <p class="emphasise">Describe why your users might want to use the type of technology tool you are thinking about. What might stop them from using it?</p>
              <textarea name="step2" class="form-control" rows="12"></textarea>
            </div>

            <div class="col-md-5 col-sm-3 col-xs-12">
              <img src="/assets/images/six-rules/Step1.svg" class="img-responsive center-block" alt="Research!">
              <div class="box box-research">
                <h3>Research Finding</h3>
                Only 3 organisations of the 38 we interviewed did research on all three questions. Many were not prepared for technical challenges that came later on, and less than one-quarter were happy with the tool they chose.
              </div>
            </div>
          </div>

          <div class="row rule">
            <div class="col-md-7 col-sm-9 col-xs-12">
              <h2>2. Think twice before you build</h2>
              <p>Look for existing tools that can do what you need. Building a completely new tool is complex and risky. </p>
              <p class="emphasise">List any existing tools that can do the things you want. </p>
              <textarea name="step3" class="form-control" rows="12"></textarea>
            </div>
            <div class="col-md-5 col-sm-3 col-xs-12">
              <img src="/assets/images/six-rules/Step2.svg" class="img-responsive center-block" alt="Research!">
              <div class="box box-research">
                <h3>Research Finding</h3>
                More than half the organisations in our research built a completely new tool, often without checking if existing tools could do the job. Developing a new tool from scratch usually took much longer and cost more than expected. New tools were often not used by their target audience.
              </div>
            </div>
          </div>

          <div class="row rule">
            <div class="col-md-7 col-sm-9 col-xs-12">
              <h2>3. Get a second opinion</h2>
              <p>Someone else has probably tried a similar approach before you. Find them, and ask for advice. </p>
              <p class="emphasise">Write down any people or organisations that have used a similar tool in their projects.</p>
              <textarea name="step4" class="form-control" rows="12"></textarea>
            </div>
            <div class="col-md-5 col-sm-3 col-xs-12">
              <img src="/assets/images/six-rules/Step3.svg" class="img-responsive center-block" alt="Research!">
              <div class="box box-research">
                <h3>Research Finding</h3>
                Organisations often underestimated the time and effort they needed to put into choosing and using a tool. Talking to people with practical experience on similar projects often helped them avoid mistakes.
              </div>
            </div>
          </div>

          <div class="row rule">
            <div class="col-md-7 col-sm-9 col-xs-12">
              <h2>4. Always take it for a test drive</h2>
              <p>Try out at least one tool, with the people you want to use it, before choosing. Trialling highlights problems at the start. It also raises questions you never knew you had.</p>
              <p class="emphasise">List any people who you could trial the tool with. </p>
              <textarea name="step5" class="form-control" rows="12"></textarea>
            </div>
            <div class="col-md-5 col-sm-3 col-xs-12">
              <img src="/assets/images/six-rules/Step4.svg" class="img-responsive center-block" alt="Research!">
              <div class="box box-research">
                <h3>Research Finding</h3>
                Only 20% of the organisations trialled tools before choosing them. Those that did said it was central to their tool’s success. Trialling wasn’t always successful, but this was usually because the organisation did not test the tool with the people who would eventually use it.
              </div>
            </div>
          </div>

          <div class="row rule">
            <div class="col-md-7 col-sm-9 col-xs-12">
              <h2>5. Plan for failure</h2>
              <p>You will not get it right first time. Budget and plan to make regular adjustments to the tool throughout the project. </p>
              <p class="emphasise">Write down a short plan for how you will adjust your project plan if unexpected changes occur. Include a testing phase and time to make improvements.</p>
              <textarea name="step6" class="form-control" rows="12"></textarea>
            </div>
            <div class="col-md-5 col-sm-3 col-xs-12">
              <img src="/assets/images/six-rules/step5.svg" class="img-responsive center-block" alt="Research!">
              <div class="box box-research">
                <h3>Research Finding</h3>
                The few organisations that explicitly planned for the risks involved in choosing new tools were most likely to be happy with their choice. They also gained knowledge that they said they would be useful next time they chose a tool.
              </div>
            </div>
          </div>

          <div class="row rule">
            <div class="col-md-7 col-sm-9 col-xs-12">
              <h2>6. Reflect on what you’re doing</h2>
              <p>Keep thinking about what is and isn’t working. Apply what you learn to your organisation’s work, and share with other organisations.</p>
              <p></p>
              <p class="emphasise">Download your answers in a document that you can print and share.</p>

