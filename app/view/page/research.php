            <h1>Research summary</h1>
            <h2>How Kenyan and South African transparency and accountability initiatives choose digital technology tools</h2>
          </header>
          <p class="introduction"><span></span>In 2015, The Engine Room, the Network Society Lab and Mtaani Initiative interviewed 38 organisations in South Africa and Kenya that had chosen digital technology tools to use in projects that aimed to increase transparency and accountability. </p>
          <p class="introduction">We asked why they had chosen a particular tool, how they chose it, and if they were happy with the results. </p>
          <h2>What did we find?</h2>
