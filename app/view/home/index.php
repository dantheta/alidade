            <div class="col-md-12">
                <h2>If you need help, you are not alone</h2>
                <p>We interviewed 40 organisations in Kenya and South Africa that work on transparency, citizen reporting and increasing government accountability. All of them described a time when they had chosen a technology tool over the last year. These tools typically played an integral role in deciding whether a project would succeed or fail. Despite this, most did limited research, rarely chose from more than one option, and almost never tested the tool with the people they hoped would use it.</p>
                <p>The most common reasons for failure were:</p>
                <ul>
                    <li>The tool didn’t work as the organisation expected it to – or it didn’t work at all.</li>
                    <li>The tool’s intended users didn’t use it in the way the organisation had hoped.</li> 
                    <li>The project took much longer or cost much more than planned (or both).</li>
                    <li>The organisation was unable to find a technical partner that they could work with well.</li>
                </ul>
            </div>
            <div class="col-md-11 col-md-offset-1">
                <blockquote>
                    <p>
                    We didn't plan properly what we really wanted. And we didn't look at other options&hellip;<br />
                    Next time we would consult more widely and not assume we knew what we wanted.
                    </p>
                </blockquote>
                
                <blockquote>
                    <p>
                    We had decided we were going to use SMS, but we did not do any research.<br />
                    The use of the tool went poorly&hellip; the efficiency we thought we’d have is not there.
                    </p>
                </blockquote>
            </div>
            <div class="col-md-12">
                <p>The organisations that made successful selections picked a tool that matched the time, resources and technical capacity they had available: they kept things as simple as they could.</p>
                <p>So this [guide], based on real-life examples, walks you through some steps to select a digital tool for a project. It focuses on our key finding – that if you avoid hard questions at the start, you will probably face bigger headaches later on. </p>
